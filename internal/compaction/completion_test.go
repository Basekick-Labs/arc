// --- writeCompletionManifest ---

func TestWriteCompletionManifest_HappyPath(t *testing.T) {
	dir := t.TempDir()
	m := newTestManifest("db_cpu_123", CompletionStateOutputWritten)

	if err := writeCompletionManifest(dir, m); err != nil {
		t.Fatalf("writeCompletionManifest: %v", err)
	}

	final := filepath.Join(dir, m.JobID+".json")
	if _, err := os.Stat(final); err != nil {
		t.Fatalf("final file missing: %v", err)
	}
	// No tmp file left behind after a clean write.
	if _, err := os.Stat(final + ".tmp"); !os.IsNotExist(err) {
		t.Fatalf("tmp file should not exist after clean write: %v", err)
	}

	// File permissions should be 0600.
	info, err := os.Stat(final)
	if err != nil {
		t.Fatalf("stat: %v", err)
	}
	if perm := info.Mode().Perm(); perm != 0o600 {
		t.Errorf("file perm: got %o, want 0600", perm)
	}
}

