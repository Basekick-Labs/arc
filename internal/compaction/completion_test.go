func TestWriteCompletionManifest_OverwritesExisting(t *testing.T) {
	dir := t.TempDir()
	m := newTestManifest("db_cpu_789", CompletionStateOutputWritten)

	// First write: state=output_written
	if err := writeCompletionManifest(dir, m); err != nil {
		t.Fatalf("first write: %v", err)
	}

	// Transition to sources_deleted and rewrite.
	m.State = CompletionStateSourcesDeleted
	m.DeletedSources = []string{"a.parquet", "b.parquet"}
	m.UpdatedAt = m.UpdatedAt.Add(time.Second)
	if err := writeCompletionManifest(dir, m); err != nil {
		t.Fatalf("second write: %v", err)
	}

	got, err := readCompletionManifest(filepath.Join(dir, m.JobID+".json"))
	if err != nil {
		t.Fatalf("readCompletionManifest: %v", err)
	}
	if got.State != CompletionStateSourcesDeleted {
		t.Errorf("state: got %q, want %q", got.State, CompletionStateSourcesDeleted)
	}
	if len(got.DeletedSources) != 2 {
		t.Errorf("DeletedSources: got %d, want 2", len(got.DeletedSources))
	}
}

