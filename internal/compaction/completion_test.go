// TestWriteCompletionManifest_RejectsPathTraversalInJobID exhaustively
// exercises every branch of validateJobID. Regression test for the
// Phase 4 security-review finding: the original check only rejected
// forward/back slashes, missing null bytes, "..", and leading dots.
// All four branches must reject so a malicious or buggy upstream JobID
// cannot escape the pending directory via filepath.Join.
func TestWriteCompletionManifest_RejectsPathTraversalInJobID(t *testing.T) {
	cases := []struct {
		name       string
		jobID      string
		wantSubstr string
	}{
		{"forward_slash", "foo/bar", "path separators"},
		{"backslash", "foo\\bar", "path separators"},
		{"absolute_forward", "/absolute", "path separators"},
		{"null_byte", "foo\x00bar", "null bytes"},
		{"relative_dotdot", "foo..bar", "'..'"},
		{"dotdot_only", "..", "'..'"},
		{"dotdot_trailing", "safe..", "'..'"},
		{"leading_dot", ".hidden", "'.'"},
		{"leading_dot_only", ".", "'.'"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			m := newTestManifest(tc.jobID, CompletionStateOutputWritten)
			err := writeCompletionManifest(t.TempDir(), m)
			if err == nil {
				t.Fatalf("expected error for JobID %q, got nil", tc.jobID)
			}
			if !strings.Contains(err.Error(), tc.wantSubstr) {
				t.Errorf("error for %q should mention %q, got: %v", tc.jobID, tc.wantSubstr, err)
			}
		})
	}
}

// TestWriteCompletionManifest_AcceptsValidJobIDs confirms the validator
