// --- concurrent write/read (watcher polling during subprocess writes) ---

func TestConcurrentWriteAndRead(t *testing.T) {
	// Simulates the real-world pattern: subprocess rewrites the manifest
	// repeatedly (state transitions) while the watcher polls listPending +
	// readCompletionManifest concurrently. The atomic rename ensures the
	// reader either sees the previous state or the new state, never a torn
	// value. We can't reliably observe a torn read (it would be a bug), so
	// this test asserts the reader never sees a json.Unmarshal error.
	dir := t.TempDir()
	jobID := "concurrent_job"

	var wg sync.WaitGroup
	stop := make(chan struct{})

	// Writer: repeatedly rewrite the manifest, bumping UpdatedAt.
	wg.Add(1)
	go func() {
		defer wg.Done()
		m := newTestManifest(jobID, CompletionStateWritingOutput)
		for i := 0; ; i++ {
			select {
			case <-stop:
				return
			default:
			}
			m.UpdatedAt = m.UpdatedAt.Add(time.Millisecond)
			if i%3 == 1 {
				m.State = CompletionStateOutputWritten
			} else if i%3 == 2 {
				m.State = CompletionStateSourcesDeleted
			} else {
				m.State = CompletionStateWritingOutput
			}
			if err := writeCompletionManifest(dir, m); err != nil {
				t.Errorf("write iteration %d: %v", i, err)
				return
			}
		}
	}()

	// Reader: poll and try to read the manifest. Any unmarshal failure is
	// a torn read and indicates a bug in the atomic-rename pattern.
	wg.Add(1)
	go func() {
		defer wg.Done()
		deadline := time.Now().Add(200 * time.Millisecond)
		readCount := 0
		for time.Now().Before(deadline) {
			paths, err := listPendingCompletionManifests(dir)
			if err != nil {
				t.Errorf("list: %v", err)
				return
			}
			for _, p := range paths {
				if _, err := readCompletionManifest(p); err != nil {
					// A torn-read would fail json.Unmarshal here. That's
					// the bug we're guarding against.
					t.Errorf("torn read of %s: %v", p, err)
					return
				}
				readCount++
			}
		}
		if readCount == 0 {
			t.Errorf("reader never observed a manifest; timing issue in test")
		}
	}()

	// Let both goroutines run briefly, then stop.
	time.Sleep(200 * time.Millisecond)
	close(stop)
	wg.Wait()
}

